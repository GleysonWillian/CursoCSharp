using System;

class Program
{
    static void Main()
    {
        int num = 0; // Inicializa a variável num com valor 0

        while (num < 20) // Condição: enquanto num for menor que 20
        {
            num++; // Incrementa o valor de num em 1
            Console.WriteLine(num); // Exibe o valor atual de num
        }

        Console.WriteLine("Fim do programa!");
    }
}
